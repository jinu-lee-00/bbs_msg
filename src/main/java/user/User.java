package user;

public class User {
	private String userID;
	private String userPassword;
	private String userName;
	private String userNickname;
	private String userGender;
	private String userEmail;
	private String userSmoke;
	private String userMarried;
	private String userEdu;
	private String userIncome;
	
	public String getUserNickname() {
		return userNickname;
	}
	public void setUserNickname(String userNickname) {
		this.userNickname = userNickname;
	}
	public String getUserSmoke() {
		return userSmoke;
	}
	public void setUserSmoke(String userSmoke) {
		this.userSmoke = userSmoke;
	}
	public String getUserMarried() {
		return userMarried;
	}
	public void setUserMarried(String userMarried) {
		this.userMarried = userMarried;
	}
	public String getUserEdu() {
		return userEdu;
	}
	public void setUserEdu(String userEdu) {
		this.userEdu = userEdu;
	}
	public String getUserIncome() {
		return userIncome;
	}
	public void setUserIncome(String userIncome) {
		this.userIncome = userIncome;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
}
